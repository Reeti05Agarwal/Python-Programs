#Wap to take a number and print day name. asuume the day starts from monday.
dnum = int(input("enter day no. : "))
if dnum == 1:
    print("MONDAY")
elif dnum == 2:
    print("TUESDAY")
elif dnum == 3:
    print("WEDNESSDAY")
elif dnum == 4:
    print("THURSDAY")
elif dnum == 5:
    print("FRIDAY")
elif dnum == 6:
    print("SATURDAY")
elif dnum == 7:
    print("SUNDAY")
else:
    print("please enter a valid input between 1 to 7")    
